class Solution {
  public:
    int maxPartitions(vector<int>& arr) {
        // code here
        int cnt=0,max_seen=0;
        for(int i=0;i<arr.size();i++){
            max_seen = max(max_seen,arr[i]);
            if(max_seen==i) cnt++;
        }
        return cnt;
    }
};


